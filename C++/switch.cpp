#include <bits/stdc++.h>
using namespace std;



int main(){

    char c;
    cin>>c;
    switch (c)
    {
    case 'a':
        cout<<"A a"<<endl;
        break;
    case 'b':
        cout<<"B b"<<endl;
        break;
    case 'c':
        cout<<"C c"<<endl;
        break;
    case 'd':
        cout<<"D d"<<endl;
        break;
    default:
        cout<<char(c-32)<<" "<<c<<endl;
        break;
    }

    

    return 0;
}





