#include<bits/stdc++.h>
using namespace std;



/*
infinite loop
*/
int main(){
    int x=0;
    for(;;){
        cout<<x<<" ";
        ++x;
        if(x==10){
            break;
        }
    }
    cout<<endl;

    int y=0;
    while(1){
        cout<<y<<" ";
        ++y;
        if(y>=10){
            break;
        }
    }
    cout<<endl;

    for(int i=1;;++i){
        cout<<i<<" ";
        if(i>=15){
            break;
        }
    }
    cout<<endl;

    return 0;

}




